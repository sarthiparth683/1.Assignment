// 8. includes()
// The includes() method checks if an array includes a certain element and returns true or false(like Boolean). It's like checking if a specific ingredient exists in a recipe.

let fruits = ['apple', 'banana', 'orange'];
let includesBanana = fruits.includes('banana');
let includesBanana1 = fruits.includes('banana1');
console.log(includesBanana);
console.log(includesBanana1);
 // Output: true
 