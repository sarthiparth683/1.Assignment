function compareTwoNumbers(num1, num2) {
    if (num1 > num2) {
        console.log("true");
    } else {
        console.log("false");
    }
    if (num1 < num2) {
        console.log("true");
    } else {
        console.log("false");
    }
    if (num1 == num2) {
        console.log("true");
    } else {
        console.log("false");
    }
}
// Calling Function
compareTwoNumbers(4, 16);
