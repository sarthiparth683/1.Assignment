 // While Loop concept
 let sum = 0; 
 let i = 1; // initialiazation condition or starting
 let n = 5;  
 while (i <= n) {  // condition to stop or Ending  condition
    sum = sum + i;     // logic or code 
    i++;              // inc/dec
 }
 console.log(sum);









