// Local Storage:-
// Basic Operations:
localStorage.setItem(key, value); //To store data.
localStorage.getItem(key); // To retrieve stored data.
localStorage.removeItem(key); // To delete specific data by key.
localStorage.clear(); // To clear all local storage data.
//------------------------------------------------------------------------------------
// JSON and Data Handling:-
// Solution:
JSON.stringify(); //to convert (arrays or objects) into a (JSON string format) before storing them in local storage.
JSON.parse(); //to convert (JSON strings) back into (JavaScript objects or arrays) for data manipulation and usage.